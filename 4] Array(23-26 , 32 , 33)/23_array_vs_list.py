# array vs list

# List in Python

# A list is a built-in data structure.
# Can store different data types.
# Size is dynamic (can grow or shrink).
# More flexible and commonly used.

# Example:
lst = [10, "apple", 3.5]
print(lst)

# 🔹 Array in Python

# Arrays are provided by the array module.
# Can store only one type of data.
# More memory-efficient than lists.
# Less flexible than lists.

# Example:
import array
arr = array.array('i', [10, 20, 30])
print(arr)